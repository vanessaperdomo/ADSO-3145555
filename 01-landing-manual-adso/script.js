const activityContainer = document.getElementById('activityContainer');
const searchInput = document.getElementById('searchInput');
const filterButtons = document.querySelectorAll('.filter-btn');

let selectedPhase = 'all';

function normalizeText(value) {
  return (value || '')
    .toString()
    .normalize('NFD')
    .replace(/[\u0300-\u036f]/g, '')
    .toLowerCase();
}

function activityMatchesSearch(activity, query) {
  const blob = [
    activity.phase,
    activity.title,
    activity.folder,
    activity.description,
    activity.duration,
    ...activity.competencies.map(item => `${item.code} ${item.name}`),
    ...activity.raps.map(item => `${item.code} ${item.name}`),
    ...activity.evidences,
    ...activity.criteria
  ].join(' ');
  return normalizeText(blob).includes(normalizeText(query));
}

function renderActivities() {
  const query = searchInput.value.trim();
  const filtered = activityData.filter(activity => {
    const phaseOk = selectedPhase === 'all' || activity.phase === selectedPhase;
    const searchOk = !query || activityMatchesSearch(activity, query);
    return phaseOk && searchOk;
  });

  if (!filtered.length) {
    activityContainer.innerHTML = '<div class="empty-state">No se encontraron actividades con el filtro actual.</div>';
    return;
  }

  activityContainer.innerHTML = filtered.map(activity => {
    const competencyItems = activity.competencies.map(item => `
      <li><strong>${item.code}</strong> · ${item.name}</li>
    `).join('');

    const rapItems = activity.raps.map(item => `
      <li><strong>${item.code}</strong> · ${item.name}</li>
    `).join('');

    const evidenceItems = activity.evidences.map(item => `<li>${item}</li>`).join('');
    const criteriaItems = activity.criteria.map(item => `<li>${item}</li>`).join('');

    return `
      <article class="activity-card">
        <div class="activity-top">
          <div>
            <div class="phase-badge">${activity.phase}</div>
            <h3 class="activity-title">${activity.title}</h3>
            <p class="activity-desc">${activity.description}</p>
          </div>
          <div class="folder-badge">${activity.folder}</div>
        </div>

        <div class="activity-sections">
          <section class="mini-panel">
            <h4>Competencia</h4>
            <ul class="item-list">${competencyItems}</ul>
          </section>

          <section class="mini-panel">
            <h4>RAP</h4>
            <ul class="item-list">${rapItems}</ul>
          </section>

          <section class="mini-panel">
            <h4>Actividades de aprendizaje</h4>
            <ul class="tag-list">
              <li class="tag">01-learning-guide</li>
              <li class="tag">02-learning-activity</li>
              <li class="tag">03-support-material</li>
              <li class="tag">04-evidence-learning</li>
            </ul>
            <p class="activity-desc activity-meta">Duración estimada: ${activity.duration || 'Pendiente'}</p>
          </section>

          <section class="mini-panel">
            <h4>Evidencias</h4>
            <ul class="item-list">${evidenceItems}</ul>
          </section>

          <section class="mini-panel">
            <h4>Criterios de evaluación</h4>
            <ul class="item-list">${criteriaItems}</ul>
          </section>

          <section class="mini-panel">
            <h4>Instrumento</h4>
            <div class="instrument">
              <strong>Pendiente:</strong> definición institucional por parte del instructor. Esta landing deja visible el punto de control para asociar lista de chequeo, rúbrica o instrumento oficial.
            </div>
          </section>
        </div>
      </article>
    `;
  }).join('');
}

filterButtons.forEach(button => {
  button.addEventListener('click', () => {
    filterButtons.forEach(item => item.classList.remove('active'));
    button.classList.add('active');
    selectedPhase = button.dataset.phase;
    renderActivities();
  });
});

searchInput.addEventListener('input', renderActivities);

renderActivities();
